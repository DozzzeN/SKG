def sum_powers_of_log(rl, rr, proportion):
    # 防止精度溢出
    total_sum = Decimal(0)
    sum_proportion = sum(Decimal(v) for v in proportion.values())
    for i in range(rl, rr + 1):
        if i not in proportion:
            continue
        log_value = np.ceil(np.log2(i))
        power_value = Decimal(2) ** (Decimal(i) * Decimal(log_value)) * Decimal(proportion[i]) / Decimal(sum_proportion)
        total_sum += power_value
    return total_sum


